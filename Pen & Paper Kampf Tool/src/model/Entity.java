package model;

public class Entity
	{
		private String name;
		private int ini;
		private int hp;
		
		//getter/setter
		public String getName()
			{
				return name;
			}
		public void setName(String name)
			{
				this.name = name;
			}
		public int getIni()
			{
				return ini;
			}
		public void setIni(int ini)
			{
				this.ini = ini;
			}
		public int getHp()
			{
				return hp;
			}
		public void setHp(int hp)
			{
				this.hp = hp;
			}
	}
